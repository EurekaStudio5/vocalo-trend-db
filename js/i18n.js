/* 単位の翻訳 */
const UNITS_T = {
  en: { "人": " subs", "回": "", "本": "", "曲": " songs", "種": " vocals",
        "文字": " chars", "期間": " periods", "点": " pts", "回/日": "/day",
        "人/年": " subs/yr", "本/月": "/mo" },
  zh: { "人": "", "回": "次", "本": "支", "曲": "首", "種": "种",
        "文字": "字", "期間": "个榜", "点": "分", "回/日": "次/日",
        "人/年": "/年", "本/月": "支/月" },
  ko: { "人": "명", "回": "회", "本": "개", "曲": "곡", "種": "종",
        "文字": "자", "期間": "개", "点": "점", "回/日": "회/일",
        "人/年": "명/년", "本/月": "개/월" },
};
function tUnit(u) {
  if (LANG === "ja") return u;
  const m = UNITS_T[LANG] || {};
  return m[u] !== undefined ? m[u] : u;
}

/* 順位表示 */
function rankLabel(n) {
  return LANG === "ja" ? `${n}位` : LANG === "zh" ? `第${n}` : LANG === "ko" ? `${n}위` : `#${n}`;
}

/* 相対時刻（YouTube由来の日本語「3日前」の代わりに daysAgo から生成） */
function relTime(daysAgo) {
  if (daysAgo == null) return "";
  const d = Math.max(daysAgo, 0);
  let n, u;
  if (d < 1) { n = Math.max(1, Math.round(d * 24)); u = "h"; }
  else if (d < 30) { n = Math.round(d); u = "d"; }
  else if (d < 365) { n = Math.round(d / 30); u = "mo"; }
  else { n = Math.round(d / 365 * 10) / 10; u = "y"; }
  const F = {
    ja: { h: `${n}時間前`, d: `${n}日前`, mo: `${n}か月前`, y: `${n}年前` },
    en: { h: `${n}h ago`, d: `${n}d ago`, mo: `${n}mo ago`, y: `${n}y ago` },
    zh: { h: `${n}小时前`, d: `${n}天前`, mo: `${n}个月前`, y: `${n}年前` },
    ko: { h: `${n}시간 전`, d: `${n}일 전`, mo: `${n}개월 전`, y: `${n}년 전` },
  };
  return F[LANG][u];
}

/* 静的HTMLへの適用 */
function applyStaticI18n() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPh);
  });
  // 言語別表示ブロック（About等）
  document.querySelectorAll("[data-lang]").forEach((el) => {
    el.style.display = el.dataset.lang === LANG ? "" : "none";
  });
}

document.addEventListener("DOMContentLoaded", () => {
  applyStaticI18n();
  const sel = document.getElementById("langSelect");
  if (sel) {
    sel.value = LANG;
    sel.addEventListener("change", () => {
      localStorage.setItem("vtdb_lang", sel.value);
      location.reload();
    });
  }
});
